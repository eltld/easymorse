package com.easymorse.util.image;

public class ImageRuntimeException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public ImageRuntimeException() {
		super();
	}

	public ImageRuntimeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ImageRuntimeException(String arg0) {
		super(arg0);
	}

	public ImageRuntimeException(Throwable arg0) {
		super(arg0);
	}

}
